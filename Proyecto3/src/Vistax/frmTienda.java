    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int x=-1;
        try {
            x = Integer.parseInt(JOptionPane.showInputDialog(rootPane, "Ingrese Codigo de la Tienda : ", "Ingrese", JOptionPane.INFORMATION_MESSAGE));
            tienda=new Tienda();
            Object o[][]=control.seekUniversal(tienda.getVectorData(), "Tienda",x+"");
            if(o!=null){
                tienda = new Tienda(o);
                txtTiendaID.setText(tienda.getTiendaID()+"");
                txtNombre.setText(tienda.getNombre());
                txtEncargado.setText(tienda.getEncargado());
                txtDireccion.setText(tienda.getDireccion());
                txtNombre.grabFocus();
                ins = false;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Debe de ingresar un valor numerico, pulse de nuevo el boton Recuperar", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        boolean a=false;
        for(JTextField o:txtCampos){
            if(o.getText().isEmpty()){
                JOptionPane.showMessageDialog(rootPane,"Campo "+o.getName()+" se encuentra Vacio","Error",JOptionPane.ERROR_MESSAGE);
                o.grabFocus();
                a=false;
                break;
            }
            else{
                a=true;
            }
        }
        if(a){
            tienda=new Tienda(inter.String_Int(txtTiendaID.getText()), txtNombre.getText().toUpperCase(),txtEncargado.getText().toUpperCase(),txtDireccion.getText().toUpperCase());
            if(ins){
                control.insUniversal(tienda.getVectorData(), "Tienda");
            }
            else{
                control.updUniversal(tienda.getVectorData(),"Tienda",txtTiendaID.getText().trim());
            }
            Limpiar_Campos();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int res = -1;
        if (!ins) {
            res = JOptionPane.showConfirmDialog(rootPane, "Â¿Desea Eliminar este Registro?", "Mensaje", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (res == JOptionPane.YES_OPTION) {
                control.delUniversal(tienda.getVectorData(), "Tienda", txtTiendaID.getText().trim());
                Limpiar_Campos();
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

